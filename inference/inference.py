def plot_comparison(products):
    labels = [f"P{i+1}" for i in range(len(products))]
    calories = [to_float(p["calories"]) for p in products]
    protein = [to_float(p["protein"]) for p in products]
    carbs = [to_float(p["carbs"]) for p in products]

    x = range(len(products))

    plt.figure()
    plt.bar(x, calories)
    plt.xticks(x, labels)
    plt.title("Calories Comparison")

    plt.figure()
    plt.bar(x, protein)
    plt.xticks(x, labels)
    plt.title("Protein Comparison")

    plt.figure()
    plt.bar(x, carbs)
    plt.xticks(x, labels)
    plt.title("Carbs Comparison")

    plt.show()